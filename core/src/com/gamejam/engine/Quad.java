package com.gamejam.engine;

public class Quad
{
	public float x;
	public float y;
	public float w;
	public float h;
	
	public Quad()
	{
		this.x = 0;
		this.y = 0;
		this.w = 0;
		this.h = 0;
	}
	
	public Quad(float x, float y, float w, float h)
	{
		this.x = x;
		this.y = y;
		this.w = w;
		this.h = h;
	}

}
